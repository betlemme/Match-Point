package it.gemmed.resource;

import it.gemmed.resource.Tiposuperficie;

/**
 * Rappresenta un campo da gioco
 * @author GEMMED
 *
 */
public class Campo {
	private final int numero;
	private final Tiposuperficie superficie;
	private final String circolo;
	
	/**
	 * Costruttore di un campo
	 * 
	 * @param numero numero assegnato al campo
	 * @param superficie materiale di cui Ã¨ fatto il campo
	 * @param circolo circolo possessore del campo
	 */
	public Campo(int numero, Tiposuperficie superficie, String circolo) {
		super();
		this.numero = numero;
		this.superficie = superficie;
		this.circolo = circolo;
	}

	/**
	 * @return numero
	 */
	public int getNumero() {
		return numero;
	}

	/**
	 * @return superficie
	 */
	public Tiposuperficie getSuperficie() {
		return superficie;
	}

	/**
	 * @return circolo
	 */
	public String getCircolo() {
		return circolo;
	}
}
