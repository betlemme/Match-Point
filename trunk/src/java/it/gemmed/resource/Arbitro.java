package it.gemmed.resource;

import java.sql.Date;

/**
 * Rappresenta i dati dell'arbitro
 * @author GEMMED
 *
 */
public class Arbitro {
	private final String nome;
	private final String cognome;
	private final Date data_nascita;
	private String indirizzo;
	private String citta;
	private String provincia;
	private String telefono;
	private final String email;
	private String password;
	private String tessera;
	private Date scadenza_tessere;
	
	/**
	 * Costruttore completo di Arbitro
	 * 
	 * @param nome
	 * @param cognome
	 * @param data_nascita
	 * @param indirizzo
	 * @param telefono
	 * @param email funzionante anche come nome utente per il login
	 * @param password
	 * @param tessera
	 * @param scadenza_tessere
	 * @param citta
	 * @param provincia
	 */
	public Arbitro(String nome, String cognome, Date data_nascita, String indirizzo, String telefono,
			String email, String password, String tessera, Date scadenza_tessere, String citta, String provincia) {
		super();
		this.nome = nome;
		this.cognome = cognome;
		this.data_nascita = data_nascita;
		this.indirizzo = indirizzo;
		this.telefono = telefono;
		this.email = email;
		this.password = password;
		this.tessera = tessera;
		this.scadenza_tessere = scadenza_tessere;
		this.citta = citta;
		this.provincia = provincia;
	}
	/**
	 * Costruttore non completo di Arbitro
	 * 
	 * @param nome
	 * @param cognome
	 * @param data_nascita
	 * @param email funzionante anche come nome utente per il login
	 * @param password
	 */
	public Arbitro(String nome, String cognome, Date data_nascita, 	String email, String password) {
		super();
		this.nome = nome;
		this.cognome = cognome;
		this.data_nascita = data_nascita;
		this.email = email;
		this.password = password;
		
	}

	/**
	 * @return nome
	 */
	public String getNome() {
		return nome;
	}

	/**
	 * @return  cognome
	 */
	public String getCognome() {
		return cognome;
	}

	/**
	 * @return data_nascita
	 */
	public Date getData_nascita() {
		return data_nascita;
	}

	/**
	 * @return email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @return password
	 */
	public String getPassword() {
		return password;
	}
	
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}

	/**
	 * @return indirizzo
	 */
	public String getIndirizzo() {
		return indirizzo;
	}
	
	/**
	 * @return citta
	 */
	public String getCitta() {
		return citta;
	}
	
	/**
	 * @return provincia
	 */
	public String getProvincia() {
		return provincia;
	}

	/**
	 * @param indirizzo the indirizzo to set
	 */
	public void setIndirizzo(String indirizzo) {
		this.indirizzo = indirizzo;
	}

	/**
	 * @param  citta the citta to set
	 */
	public void setCitta(String citta) {
		this.citta = citta;
	}
	
	/**
	 * @param provincia the provincia to set
	 */
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	
	
	/**
	 * @return telefono
	 */
	public String getTelefono() {
		return telefono;
	}

	/**
	 * @param telefono - the telefono to set
	 */
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}

	/**
	 * @return tessera
	 */
	public String getTessera() {
		return tessera;
	}

	/**
	 * @param tessera the tessera to set
	 */
	public void setTessera(String tessera) {
		this.tessera = tessera;
	}

	/**
	 * @return scadenza_tessere
	 */
	public Date getScadenza_tessere() {
		return scadenza_tessere;
	}

	/**
	 * @param scadenza_tessere the scadenza_tessere to set
	 */
	public void setScadenza_tessere(Date scadenza_tessere) {
		this.scadenza_tessere = scadenza_tessere;
	}
}
