package it.gemmed.resource;

/**
 * Classifica storica di un giocatore
 * 
 * @author GEMMED
 *
 */
public class Storico {
	private final String giocatore;
	private final String classifica;
	private final int anno;
	
	/**
	 * Costruttore completo
	 * 
	 * @param giocatore
	 * @param classifica
	 * @param anno
	 */
	public Storico(String giocatore, String classifica, int anno) {
		super();
		this.giocatore = giocatore;
		this.classifica = classifica;
		this.anno = anno;
	}

	/**
	 * @return giocatore
	 */
	public String getGiocatore() {
		return giocatore;
	}

	/**
	 * @return classifica
	 */
	public String getClassifica() {
		return classifica;
	}

	/**
	 * @return anno
	 */
	public int getAnno() {
		return anno;
	}
	
	
}
